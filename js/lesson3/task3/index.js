/* сравнение чисел */

const a = 17;
const b = 5;

a > b;
a <= b;
a == b;
a != b;

console.log("a < b: ", a < b);
console.log("a <= b: ", a <= b);
console.log("a == b: ", a == b);
console.log("a != b: ", a != b);

/* сравнение строк */

'a' < 'b';
'ab' > 'a';

console.log("\'a\' < \'b\': ", 'a' < 'b');
console.log("\'ab\' > \'a\': ", 'ab' > 'a');

/* строгое сравнение */

17 === 1;
17 === 17;

console.log("17 === 1: ", 17 === 1);
console.log("17 === 17: ", 17 === 17);
